//计算矩阵x和y的积

/*
1-定义三个二维数组 
*/

#include<stdio.h>

void main()
{
	int x[2][3]={{1,2,3},{4,5,6}},y[3][2]={{1,5},{5,3},{8,1}};
	int z[2][2]={0},i,j;
	for(i=0;i<2;i++)
	{
		for(j=0;j<3;j++)
		{
			
			z[i][0]+=x[i][j]*y[j][0]; //不同元素乘积和 
			z[i][1]+=x[i][j]*y[j][1];
		}
	}
	//输出乘积矩阵 
	for(i=0;i<2;i++)
	{
		for(j=0;j<2;j++)
		{
			printf("%d\t",z[i][j]);
		}
		printf("\n");
	}
 } 
